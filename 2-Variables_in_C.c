#include <stdio.h>
int main()
{
  int a = 2;
  float b = 4.5;
  char c = 'abc';
  double d = 29.90;
  printf("The Integer Type Value is : %d\n", a);
  printf("The Float Type Value is : %f\n", b);
  printf("The Character Type Value is : %c\n", c);
  printf("The Double Type Value is : %lf\n", d);
  return 0;
}
